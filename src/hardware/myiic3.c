//IIC初始化
void IIC3_Init(void)
{
    GPIO_InitTypeDef GPIO_Initure;

    __HAL_RCC_GPIOA_CLK_ENABLE();   //使能GPIOA时钟
    __HAL_RCC_GPIOC_CLK_ENABLE();   //使能GPIOC时钟
    //PA8初始化设置
    GPIO_Initure.Pin = GPIO_PIN_8;
    GPIO_Initure.Mode = GPIO_MODE_OUTPUT_PP; //推挽输出
    GPIO_Initure.Pull = GPIO_PULLUP;        //上拉
    GPIO_Initure.Speed = GPIO_SPEED_FAST;   //快速
    HAL_GPIO_Init(GPIOA, &GPIO_Initure);

    //PC9初始化设置
    GPIO_Initure.Pin = GPIO_PIN_9;
    GPIO_Initure.Mode = GPIO_MODE_OUTPUT_PP; //推挽输出
    GPIO_Initure.Pull = GPIO_PULLUP;        //上拉
    GPIO_Initure.Speed = GPIO_SPEED_FAST;   //快速
    HAL_GPIO_Init(GPIOC, &GPIO_Initure);

    IIC3_SDA = 1;
    IIC3_SCL = 1;
}

//产生IIC起始信号
void IIC3_Start(void)
{
    SDA3_OUT();     //sda线输出
    IIC3_SDA = 1;
    IIC3_SCL = 1;
    delay_us(4);
    IIC3_SDA = 0; //START:when CLK is high,DATA change form high to low
    delay_us(4);
    IIC3_SCL = 0; //钳住I2C总线，准备发送或接收数据
}
//产生IIC停止信号
void IIC3_Stop(void)
{
    SDA3_OUT();//sda线输出
    IIC3_SCL = 0;
    IIC3_SDA = 0; //STOP:when CLK is high DATA change form low to high
    delay_us(4);
    IIC3_SCL = 1;
    delay_us(4);
    IIC3_SDA = 1; //发送I2C总线结束信号
}
//等待应答信号到来
//返回值：1，接收应答失败
//        0，接收应答成功
uint8_t IIC3_Wait_Ack(void)
{
    uint8_t ucErrTime = 0;
    SDA3_IN();      //SDA设置为输入
    IIC3_SDA = 1;
    delay_us(1);
    IIC3_SCL = 1;
    delay_us(1);
    while(READ_SDA3)
    {
        ucErrTime++;
        if(ucErrTime > 250)
        {
            IIC3_Stop();
            return 1;
        }
    }
    IIC3_SCL = 0; //时钟输出0
    return 0;
}
//产生ACK应答
void IIC3_Ack(void)
{
    IIC3_SCL = 0;
    SDA3_OUT();
    IIC3_SDA = 0;
    delay_us(2);
    IIC3_SCL = 1;
    delay_us(2);
    IIC3_SCL = 0;
}
//不产生ACK应答
void IIC3_NAck(void)
{
    IIC3_SCL = 0;
    SDA3_OUT();
    IIC3_SDA = 1;
    delay_us(2);
    IIC3_SCL = 1;
    delay_us(2);
    IIC3_SCL = 0;
}
//IIC发送一个字节
//返回从机有无应答
//1，有应答
//0，无应答
void IIC3_Send_Byte(uint8_t txd)
{
    uint8_t t;
    SDA3_OUT();
    IIC3_SCL = 0; //拉低时钟开始数据传输
    for(t = 0; t < 8; t++)
    {
        IIC3_SDA = (txd & 0x80) >> 7;
        txd <<= 1;
        delay_us(2);   //对TEA5767这三个延时都是必须的
        IIC3_SCL = 1;
        delay_us(2);
        IIC3_SCL = 0;
        delay_us(2);
    }
}
//读1个字节，ack=1时，发送ACK，ack=0，发送nACK
uint8_t IIC3_Read_Byte(unsigned char ack)
{
    unsigned char i, receive = 0;
    SDA3_IN();//SDA设置为输入
    for(i = 0; i < 8; i++ )
    {
        IIC3_SCL = 0;
        delay_us(2);
        IIC3_SCL = 1;
        receive <<= 1;
        if(READ_SDA3)receive++;
        delay_us(1);
    }
    if (!ack)
        IIC3_NAck();//发送nACK
    else
        IIC3_Ack(); //发送ACK
    return receive;
}


